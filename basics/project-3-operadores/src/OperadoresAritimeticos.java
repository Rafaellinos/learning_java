
public class OperadoresAritimeticos {
	public static void main(String[] args) {
		
		int a = 17;
		int b = 5;
		
		int c = a + b;
		System.out.println(c);
		
		int d = a * b;
		int f = a / b;
		
		System.out.println(d);
		System.out.println(f);
		
		double g = 17 % 2;
		System.out.println(g);
		
		
		
	}
}
