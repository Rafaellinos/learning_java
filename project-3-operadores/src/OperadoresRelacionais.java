
public class OperadoresRelacionais {
	public static void main(String[] args) {
		int idade = 16;
		boolean result = idade > 18;
		System.out.println(result);
		idade += 5;
		result = idade > 18;
		System.out.println(result);
		/*
		 * 
		 * Operadores : 
		 *  == , !=, >, <, =>, <=
		 */
		if (idade > 8) {
			System.out.println("Maior de idade");
		}
		
	}
}
